package sil.managers;

public class CrystalEvent {
	
	private long crystalUniqueId = -1;
	private int id = -1;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public long getCrystalUniqueId() {
		return crystalUniqueId;
	}

	public void setCrystalUniqueId(long crystalUniqueId) {
		this.crystalUniqueId = crystalUniqueId;
	}

}
